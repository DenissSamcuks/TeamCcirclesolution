			     <p class="p1">Intranet means a closed network that resides on a local network. An intranet
				 acts like a website that only authorized users can access and is generally used by
				 organizations who wish to share knowledge, internal communications, and information with their colleagues.</p></div>
			
			<div class="item"><h2 class="h2"><strong>What is the diference betwen the intrenet and an intranet?</strong></h3>
			     
				 <p class="p1">The main difference between an intranet and the internet is that the former 
				 is a closed network, and the latter is a public network. In short, the internet is for all;
				 a company intranet is for a select group of people.</p></div>
				 
		    <div class="item"><h2 class="h2"><strong>What is an intranet and an extranet?</strong></h2>
			    
				<p class="p1">An intranet is a closed network designed to allow an organization to share
				information and corporate communication among its employees. An extranet is 
				similar to an intranet, but allows third-party entry, so vendors or partners can 
				experience full or selected access.</p></div>
				
			<div class="item"><h2 class="h2"><strong>Can an intranet work without the internet?</strong></h2>
			    
				<p class="p1">Former on-premise company intranets could operate without internet.
				But for a wide range of reasons, the majority of company intranet applications
				are now cloud-based, so an internet connection is necessary.</p></div>	
				
			<div class="item"><h2 class="h2"><strong>Can an intranet be accesed remotely?</strong></h2>
			    
				<p class="p1">Yes, an intranet can be accessed on a range of devices, regardless of 
				location – all that is required is an internet connection. This is ideal for 
				any organization with dispersed or front-line employees who need communication 
				tools and access to organizational information wherever they are.</p></div>	
				
